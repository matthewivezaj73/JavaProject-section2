//Creating a class.
public class VariableFun {
    //Our main method.
    public static void main(String[] args) {
        //Variables should start with a lowercase letter.
        //Setting variables.
        //Variable declaration with the assignment (= sign) operator.
        int my_age;
        my_age = 19;
        //Creating a symbolic constant, cannot be changed.
        final int SOME_NUM = 150;
        //Creating a variable to hold my home town.
        String home_variable = "Shelby Township.";

        //Initialization.
        String name = "Billy";
        //Println introduces another character (insertion line).
        //Println displays strings and integers.
        //Displaying the user's name and age on the screen.
        System.out.print(name + " is " + my_age+'\n');
        System.out.print("I live in " + home_variable + "\n");
    }
}
