public class ConversionFun {
    public static void main(String[] args) {
        //The 8 primitive types are,
        //byte
        //short
        //long
        //int
        //double
        //boolean
        //char
        //float

        //Making a double
        double myDouble = 3.14;
        //Doesn't matter if the f in float conversion is lowercase or uppercase.
        float myFloat = 3.14f;
        //Widening or lossless conversion.
        double yourDouble = myFloat;
    }
}
